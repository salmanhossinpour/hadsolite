export function getLevelById(id: number): WordLevel {
  if (id <= PREDEFINED_LEVELS.length) {
    return PREDEFINED_LEVELS[id - 1];
  }

  // Generate procedural level
  const poolIndex = (id - PREDEFINED_LEVELS.length - 1) % PROCEDURAL_LETTER_POOLS.length;
  const pool = PROCEDURAL_LETTER_POOLS[poolIndex];

  return {
    id,
    name: `مرحله ${id}: چالش نامحدود میرزا`,
    letters: pool.letters,
    targetWords: pool.targets,
    bonusWords: pool.bonus,
    clue: "یک چالش واژه‌سازی هوشمندانه برای استاد کلمات!"
  };
}

